package com.caasa.comexsap.exportaciones.model.to;

public class ClienteDatosVentaTO {
	private String codigo;
	private String sociedad;
	private String orgVentas;
	private String canal;
	private String sector;
	private String zonaVentas;
	private String oficinaVentas;
	private String grupoVendedor;
	private String condExpedicion;
	private String incoterm;
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public String getSociedad() {
		return sociedad;
	}
	public void setSociedad(String sociedad) {
		this.sociedad = sociedad;
	}
	public String getOrgVentas() {
		return orgVentas;
	}
	public void setOrgVentas(String orgVentas) {
		this.orgVentas = orgVentas;
	}
	public String getCanal() {
		return canal;
	}
	public void setCanal(String canal) {
		this.canal = canal;
	}
	public String getSector() {
		return sector;
	}
	public void setSector(String sector) {
		this.sector = sector;
	}
	public String getZonaVentas() {
		return zonaVentas;
	}
	public void setZonaVentas(String zonaVentas) {
		this.zonaVentas = zonaVentas;
	}
	public String getOficinaVentas() {
		return oficinaVentas;
	}
	public void setOficinaVentas(String oficinaVentas) {
		this.oficinaVentas = oficinaVentas;
	}
	public String getGrupoVendedor() {
		return grupoVendedor;
	}
	public void setGrupoVendedor(String grupoVendedor) {
		this.grupoVendedor = grupoVendedor;
	}
	public String getCondExpedicion() {
		return condExpedicion;
	}
	public void setCondExpedicion(String condExpedicion) {
		this.condExpedicion = condExpedicion;
	}
	public String getIncoterm() {
		return incoterm;
	}
	public void setIncoterm(String incoterm) {
		this.incoterm = incoterm;
	}
}
